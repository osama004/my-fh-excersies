package at.technikum.formatedoutput;

public class EscapeCharacters {
    public static void main(String[] args) {
        System.out.println("escape sequence is started with a backslash: \\\n" +
                "to print a tab ( ) use \"\\t\"\n" +
                "to print a backslash (\\) use \"\\\\\"\n" +
                "to print double quotes (\") use \"\\\"\"\n" +
                "to print a single quote (') use \"\\'\"\n" +
                "to start a new line (\n" +
                ") use \"\\n\"");
    }
}
